package com.partymanager.finalproject.dto;

public class CoinModifier {
	
	private Integer amount = 0;

	
	public CoinModifier() {
		super();
	}

	public CoinModifier(Integer amount) {
		super();
		this.amount = amount;
	}

	public Integer getAmount() {
		return amount;
	}

	public void setAmount(Integer amount) {
		this.amount = amount;
	}
	
	

}
